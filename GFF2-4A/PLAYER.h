#pragma once
#include"PadInput.h"

enum class PLAYER_STATE {
	IDLE = 0,//停止しているとき
	MOVE,    //動いてるとき
	JUMP,    //跳ぶ
	THROW,   //投げる時
	HOOK,    //フックをつかむとき
	DAMAGE,  //ダメージを受けた時
	DEAD     //死んだとき
};

class Player
{

private:
	float player_x,player_y;
	int map_x, map_y;
	int life;
	int image[100];
	int move_type;			//左か右の移動(反転用)
	int animation_frame;	//アニメーションのフレームのカウント
	int animation_type;		//今のアニメーションの添え字
	int jump_mode;			//停止ジャンプ(1)か移動ジャンプ(2)か

	PLAYER_STATE player_state;		//ステート変数

	const int MAX_LIFE = 5;
	const int DEVIATION = 2000;				//スティック入力の誤入力の範囲
	const int ANIMATION_SWITCH_FRAME = 10;	//画像を切り替えるタイミング
	const int IMAGE_MAX_NUM = 5;			//画像の枚数
public:
	Player();

	void Move();
	void Draw() const;
	void HookMove();
	void JumpMove();
	void Throw();
	void MoveAnimation();
	void Update();


	/*変数のセットとゲット*/
	int GetLife() { return life; };

	void SetLife(int);
};

